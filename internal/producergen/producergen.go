// Package producergen turns a designed model into example producer code in
// several languages (docs/TESTSTUDIO.md §9, roadmap WP-7): the code that appends
// the model's events to a Clio instance.
//
// Per event type it emits a typed payload carrier and a send function that fills
// a CloudEvents envelope (type, subject, data) and POSTs it to Clio's public
// events API, reading the base URL and bearer token from the environment.
//
// These are deliberately *scaffolds*, not a maintained SDK (§9.3): copy-paste
// starting points that show the shape. They are generated from the draft (the
// source of truth), like the schema/doc export. The Go output is run through
// go/format so it is always valid, gofmt'd code.
package producergen

import (
	"encoding/json"
	"fmt"
	"go/format"
	"strings"

	"github.com/pblumer/clio-workbench/internal/model"
)

// eventsPath is Clio's public append endpoint (structured CloudEvents POST).
const eventsPath = "/api/v1/events"

// Lang describes a supported target language/platform.
type Lang struct {
	ID       string // stable id used by the UI and Generate
	Title    string // display name
	Filename string // suggested download filename
}

// Languages returns the supported targets in display order (docs/TESTSTUDIO.md
// §9.2; v1 ships a small, maintained set per open question §12.7).
func Languages() []Lang {
	return []Lang{
		{ID: "go", Title: "Go", Filename: "producer.go"},
		{ID: "ts", Title: "TypeScript (fetch)", Filename: "producer.ts"},
		{ID: "python", Title: "Python", Filename: "producer.py"},
		{ID: "curl", Title: "curl / Shell", Filename: "producer.sh"},
	}
}

// SupportedLang reports whether id is a known language.
func SupportedLang(id string) bool {
	for _, l := range Languages() {
		if l.ID == id {
			return true
		}
	}
	return false
}

// eventType is the projection of a draft's event step used for code generation.
type eventType struct {
	Name   string
	Fields []model.Field
}

// eventTypes returns the named event types of the draft, in outline order.
func eventTypes(d model.Draft) []eventType {
	var out []eventType
	for _, st := range d.Steps {
		if st.Kind != model.StepEvent {
			continue
		}
		name := strings.TrimSpace(st.Name)
		if name == "" {
			continue
		}
		var fields []model.Field
		for _, f := range st.Fields {
			if strings.TrimSpace(f.Name) != "" {
				fields = append(fields, f)
			}
		}
		out = append(out, eventType{Name: name, Fields: fields})
	}
	return out
}

// Generate renders the producer code for the given language.
func Generate(d model.Draft, lang string) (string, error) {
	types := eventTypes(d)
	switch lang {
	case "go":
		return genGo(d, types)
	case "ts":
		return genTS(d, types), nil
	case "python":
		return genPython(d, types), nil
	case "curl":
		return genCurl(d, types), nil
	default:
		return "", fmt.Errorf("unsupported language %q", lang)
	}
}

// ── Go ────────────────────────────────────────────────────────────────────

func genGo(d model.Draft, types []eventType) (string, error) {
	var b strings.Builder
	fmt.Fprintf(&b, "// Code generated by Clio Workbench Test Studio — example scaffold, edit freely.\n")
	fmt.Fprintf(&b, "// Producer for model %q. Appends CloudEvents to a Clio instance's public API.\n", d.Name)
	if d.SubjectStyle != "" {
		fmt.Fprintf(&b, "// Subject style: %s\n", d.SubjectStyle)
	}
	b.WriteString(`package producer

import (
	"bytes"
	"crypto/rand"
	"encoding/json"
	"fmt"
	"net/http"
	"os"
)

// Client posts CloudEvents to a Clio instance.
type Client struct {
	BaseURL string
	Token   string
	Source  string
	HTTP    *http.Client
}

// NewClient reads CLIO_URL and CLIO_API_TOKEN from the environment.
func NewClient() *Client {
	return &Client{
		BaseURL: os.Getenv("CLIO_URL"),
		Token:   os.Getenv("CLIO_API_TOKEN"),
		Source:  envOr("CLIO_SOURCE", "clio-workbench-producer"),
		HTTP:    http.DefaultClient,
	}
}

func envOr(key, def string) string {
	if v := os.Getenv(key); v != "" {
		return v
	}
	return def
}

// send posts one CloudEvent (type + subject + data) in structured mode.
func (c *Client) send(eventType, subject string, data any) error {
	payload, err := json.Marshal(data)
	if err != nil {
		return err
	}
	envelope := map[string]any{
		"specversion":     "1.0",
		"type":            eventType,
		"source":          c.Source,
		"id":              newID(),
		"subject":         subject,
		"datacontenttype": "application/json",
		"data":            json.RawMessage(payload),
	}
	body, err := json.Marshal(envelope)
	if err != nil {
		return err
	}
	req, err := http.NewRequest(http.MethodPost, c.BaseURL+"` + eventsPath + `", bytes.NewReader(body))
	if err != nil {
		return err
	}
	req.Header.Set("Content-Type", "application/cloudevents+json")
	if c.Token != "" {
		req.Header.Set("Authorization", "Bearer "+c.Token)
	}
	resp, err := c.HTTP.Do(req)
	if err != nil {
		return err
	}
	defer resp.Body.Close()
	if resp.StatusCode >= 300 {
		return fmt.Errorf("clio returned %s", resp.Status)
	}
	return nil
}

func newID() string {
	var b [16]byte
	_, _ = rand.Read(b[:])
	return fmt.Sprintf("%x-%x-%x-%x-%x", b[0:4], b[4:6], b[6:8], b[8:10], b[10:16])
}
`)

	for _, et := range types {
		typeName := pascal(et.Name) + "Data"
		fmt.Fprintf(&b, "\n// %s is the data payload of the %q event.\n", typeName, et.Name)
		fmt.Fprintf(&b, "type %s struct {\n", typeName)
		for _, f := range et.Fields {
			fmt.Fprintf(&b, "\t%s %s `json:%q`\n", pascal(f.Name), goType(f), f.Name)
		}
		b.WriteString("}\n")
		fmt.Fprintf(&b, "\n// Send%s appends an %q event for the given subject.\n", pascal(et.Name), et.Name)
		fmt.Fprintf(&b, "func (c *Client) Send%s(subject string, data %s) error {\n", pascal(et.Name), typeName)
		fmt.Fprintf(&b, "\treturn c.send(%q, subject, data)\n}\n", et.Name)
	}

	formatted, err := format.Source([]byte(b.String()))
	if err != nil {
		return "", fmt.Errorf("format generated go: %w", err)
	}
	return string(formatted), nil
}

func goType(f model.Field) string {
	switch f.Type {
	case "integer":
		return "int"
	case "number":
		return "float64"
	case "boolean":
		return "bool"
	default:
		return "string"
	}
}

// ── TypeScript (fetch) ──────────────────────────────────────────────────────

func genTS(d model.Draft, types []eventType) string {
	var b strings.Builder
	fmt.Fprintf(&b, "// Code generated by Clio Workbench Test Studio — example scaffold, edit freely.\n")
	fmt.Fprintf(&b, "// Producer for model %q. Appends CloudEvents to a Clio instance's public API.\n", d.Name)
	if d.SubjectStyle != "" {
		fmt.Fprintf(&b, "// Subject style: %s\n", d.SubjectStyle)
	}
	b.WriteString(`
const BASE = process.env.CLIO_URL ?? "";
const TOKEN = process.env.CLIO_API_TOKEN ?? "";
const SOURCE = process.env.CLIO_SOURCE ?? "clio-workbench-producer";

async function send(eventType: string, subject: string, data: unknown): Promise<void> {
  const envelope = {
    specversion: "1.0",
    type: eventType,
    source: SOURCE,
    id: crypto.randomUUID(),
    subject,
    datacontenttype: "application/json",
    data,
  };
  const res = await fetch(BASE + "` + eventsPath + `", {
    method: "POST",
    headers: {
      "Content-Type": "application/cloudevents+json",
      ...(TOKEN ? { Authorization: "Bearer " + TOKEN } : {}),
    },
    body: JSON.stringify(envelope),
  });
  if (!res.ok) throw new Error("clio returned " + res.status);
}
`)
	for _, et := range types {
		iface := pascal(et.Name) + "Data"
		fmt.Fprintf(&b, "\nexport interface %s {\n", iface)
		for _, f := range et.Fields {
			fmt.Fprintf(&b, "  %q: %s;\n", f.Name, tsType(f))
		}
		b.WriteString("}\n")
		fmt.Fprintf(&b, "\nexport function send%s(subject: string, data: %s): Promise<void> {\n", pascal(et.Name), iface)
		fmt.Fprintf(&b, "  return send(%q, subject, data);\n}\n", et.Name)
	}
	return b.String()
}

func tsType(f model.Field) string {
	switch f.Type {
	case "integer", "number":
		return "number"
	case "boolean":
		return "boolean"
	default:
		return "string"
	}
}

// ── Python (stdlib urllib) ──────────────────────────────────────────────────

func genPython(d model.Draft, types []eventType) string {
	var b strings.Builder
	fmt.Fprintf(&b, "# Code generated by Clio Workbench Test Studio — example scaffold, edit freely.\n")
	fmt.Fprintf(&b, "# Producer for model %q. Appends CloudEvents to a Clio instance's public API.\n", d.Name)
	if d.SubjectStyle != "" {
		fmt.Fprintf(&b, "# Subject style: %s\n", d.SubjectStyle)
	}
	b.WriteString(`import json
import os
import uuid
import urllib.request
from dataclasses import dataclass

BASE = os.environ.get("CLIO_URL", "")
TOKEN = os.environ.get("CLIO_API_TOKEN", "")
SOURCE = os.environ.get("CLIO_SOURCE", "clio-workbench-producer")


def send(event_type: str, subject: str, data: dict) -> None:
    envelope = {
        "specversion": "1.0",
        "type": event_type,
        "source": SOURCE,
        "id": str(uuid.uuid4()),
        "subject": subject,
        "datacontenttype": "application/json",
        "data": data,
    }
    body = json.dumps(envelope).encode()
    req = urllib.request.Request(BASE + "` + eventsPath + `", data=body, method="POST")
    req.add_header("Content-Type", "application/cloudevents+json")
    if TOKEN:
        req.add_header("Authorization", "Bearer " + TOKEN)
    with urllib.request.urlopen(req) as resp:
        if resp.status >= 300:
            raise RuntimeError(f"clio returned {resp.status}")
`)
	for _, et := range types {
		cls := pascal(et.Name)
		b.WriteString("\n\n@dataclass\nclass " + cls + ":\n")
		if len(et.Fields) == 0 {
			b.WriteString("    pass\n")
		}
		for _, f := range et.Fields {
			fmt.Fprintf(&b, "    %s: %s\n", pyIdent(f.Name), pyType(f))
		}
		fmt.Fprintf(&b, "\n\ndef send_%s(subject: str, data: %s) -> None:\n", snake(et.Name), cls)
		// Build the data dict mapping original JSON keys to dataclass attributes.
		b.WriteString("    send(" + pyStr(et.Name) + ", subject, {")
		for i, f := range et.Fields {
			if i > 0 {
				b.WriteString(", ")
			}
			fmt.Fprintf(&b, "%s: data.%s", pyStr(f.Name), pyIdent(f.Name))
		}
		b.WriteString("})\n")
	}
	return b.String()
}

func pyType(f model.Field) string {
	switch f.Type {
	case "integer":
		return "int"
	case "number":
		return "float"
	case "boolean":
		return "bool"
	default:
		return "str"
	}
}

// ── curl / shell ────────────────────────────────────────────────────────────

func genCurl(d model.Draft, types []eventType) string {
	var b strings.Builder
	fmt.Fprintf(&b, "#!/usr/bin/env bash\n")
	fmt.Fprintf(&b, "# Code generated by Clio Workbench Test Studio — example scaffold, edit freely.\n")
	fmt.Fprintf(&b, "# Producer for model %q. Appends CloudEvents to a Clio instance's public API.\n", d.Name)
	if d.SubjectStyle != "" {
		fmt.Fprintf(&b, "# Subject style: %s\n", d.SubjectStyle)
	}
	b.WriteString(`set -euo pipefail

: "${CLIO_URL:?set CLIO_URL}"
CLIO_API_TOKEN="${CLIO_API_TOKEN:-}"
CLIO_SOURCE="${CLIO_SOURCE:-clio-workbench-producer}"

# send <event-type> <subject> <data-json>
send() {
  local type="$1" subject="$2" data="$3"
  local id; id="$(uuidgen 2>/dev/null || cat /proc/sys/kernel/random/uuid)"
  local envelope
  envelope="$(cat <<JSON
{"specversion":"1.0","type":"$type","source":"$CLIO_SOURCE","id":"$id","subject":"$subject","datacontenttype":"application/json","data":$data}
JSON
)"
  curl -fsS -X POST "$CLIO_URL` + eventsPath + `" \
    -H "Content-Type: application/cloudevents+json" \
    ${CLIO_API_TOKEN:+-H "Authorization: Bearer $CLIO_API_TOKEN"} \
    -d "$envelope"
}
`)
	for _, et := range types {
		fmt.Fprintf(&b, "\n# %s\n", et.Name)
		fmt.Fprintf(&b, "send %q \"$SUBJECT\" '%s'\n", et.Name, curlSample(et.Fields))
	}
	return b.String()
}

// curlSample renders a placeholder JSON object for an event's fields.
func curlSample(fields []model.Field) string {
	var parts []string
	for _, f := range fields {
		parts = append(parts, fmt.Sprintf("%q:%s", f.Name, jsonSample(f)))
	}
	return "{" + strings.Join(parts, ",") + "}"
}

func jsonSample(f model.Field) string {
	switch f.Type {
	case "integer", "number":
		return "0"
	case "boolean":
		return "false"
	case "enum":
		if len(f.Enum) > 0 {
			return pyStr(f.Enum[0])
		}
		return `""`
	default:
		return `""`
	}
}

// ── identifier helpers ──────────────────────────────────────────────────────

// words splits an arbitrary name into alphanumeric runs.
func words(s string) []string {
	return strings.FieldsFunc(s, func(r rune) bool {
		return !(r >= 'a' && r <= 'z' || r >= 'A' && r <= 'Z' || r >= '0' && r <= '9')
	})
}

// pascal renders a PascalCase identifier, guaranteed to start with a letter.
func pascal(s string) string {
	var b strings.Builder
	for _, w := range words(s) {
		b.WriteString(upFirst(w))
	}
	return ensureAlpha(b.String(), "Event", "X")
}

// snake renders a snake_case identifier, guaranteed to start with a letter.
func snake(s string) string {
	ws := words(s)
	for i, w := range ws {
		ws[i] = strings.ToLower(w)
	}
	return ensureAlpha(strings.Join(ws, "_"), "event", "x")
}

// pyIdent is snake-case, falling back to a valid Python attribute name.
func pyIdent(s string) string { return snake(s) }

func upFirst(w string) string {
	if w == "" {
		return ""
	}
	return strings.ToUpper(w[:1]) + w[1:]
}

// ensureAlpha returns fallback if s is empty, or prefixes it if s starts with a
// digit, so the result is always a valid identifier.
func ensureAlpha(s, fallback, prefix string) string {
	if s == "" {
		return fallback
	}
	if s[0] >= '0' && s[0] <= '9' {
		return prefix + s
	}
	return s
}

// pyStr renders a double-quoted string literal (also valid JSON/shell-embedded).
func pyStr(s string) string {
	b, _ := json.Marshal(s)
	return string(b)
}
